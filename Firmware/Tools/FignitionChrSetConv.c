// UDGs repeat
void VideoCopyChrs(byte dst[kChrSetBytesPerChar][kChrSetChrs])
{
	byte row,ch;
	for(ch=0;ch<128;ch++) {	
		for(row=0;row<8;row++) {
			dst[row][ch]=gOrigCharSet[ch*kChrSetBytesPerChar+ row];
		}
	}
}

void ShowChrs(byte *dst)
{
	int totalBytes=kChrSetChrs*kChrSetBytesPerChar;
	const int kBytesPerRow=8;
	int row,col;
	for(row=0;row<totalBytes/kBytesPerRow;row++) {
		printf(" /* row=%2d */",row);
		for(col=0;col<kBytesPerRow;col++) {
			printf(" 0x%02x, ",dst[row*kBytesPerRow+col]);
		}
		printf("\n");
	}
}

int main(void)
{
	byte dst[kChrSetBytesPerChar][kChrSetChrs];
	VideoCopyChrs(dst);
	ShowChrs((byte*)dst);
	return 0;
}